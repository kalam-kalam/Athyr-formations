     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Requests\FormRequest $request)
    {
        Member::create($request->all());

        $user = User::find(1)->toArray();

        Mail::send('emails.newmember', $user, function ($message) use ($user) {
            $message->from('athyrformation@gmail.com');
            $message->to('contact@athyr.fr');
            $message->subject('Newsletter Nouvelle Inscription');
        });

        return back();
    }

